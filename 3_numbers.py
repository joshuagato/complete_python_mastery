import math

print(10 + 3)   # Addition
print(10 - 3)   # Subtraction
print(10 * 3)   # Multiplication
print(10 / 3)   # Division
print(10 // 3)  # Integer division
print(10 % 3)   # Modulo/Remainder division
print(10 ** 3)  # Exponentiation

print(round(2.9))
print(abs(-2.9))

print(math.ceil(2.2))
